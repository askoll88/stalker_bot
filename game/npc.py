def get_npc(npc_id: str) -> Optional[NPC]:
    """Получить NPC по ID"""
    return NPCS.get(npc_id)


def get_all_npcs() -> list[NPC]:
    """Получить всех NPC"""
    return list(NPCS.values())


def format_npc_list(npcs: list[NPC]) -> str:
    """Форматировать список NPC"""
    if not npcs:
        return "Никого нет поблизости"

    result = []
    for npc in npcs:
        result.append(f"{npc.name}\n{npc.description}")

    return "\n\n".join(result)
